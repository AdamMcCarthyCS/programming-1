public class IntDoubleDivision {
    public static void main(String[] args) {
        int a = 7;
        int b = 2;

        int result1 = a / b;
        double result2 = (double) a / b;

        System.out.println("int division: " + result1);
        System.out.println("double division " + result2);
    }
}
